/**
 * Initializes dynamic background
 */
function backgroundInitialization()
{
    VANTA.FOG
    ({
        el: "#background",
        highlightColor: 0xCAC7E8,
        midtoneColor: 0xBBB7Ed,
        lowlightColor: 0xE4E3EF,
        baseColor: 0xE4E3EF,
        blurFactor: 0.40,
        zoom: 1.50
    });
}